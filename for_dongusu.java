public class for_dongusu {
    public static void main(String[] args) 
    {
        

        for(int i=10;0<i;i-=2)
        {
            for(int k=1;k<=i;k+=2)
            {
               System.out.print("*"); 
            }
            
            System.out.println(" "); 
        }
        
    }
    
}
